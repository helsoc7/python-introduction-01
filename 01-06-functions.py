def begruessung(name):
    print(f"Hallo, {name}!")
    
begruessung("Alice")  # Ausgabe: Hallo, Alice!
def addiere(x, y):
    return x + y

ergebnis = addiere(10, 20)
print(ergebnis)  # Ausgabe: 30
def beschreibe_person(name, alter=30):
    print(f"{name} ist {alter} Jahre alt.")
    
beschreibe_person("Bob")  # Ausgabe: Bob ist 30 Jahre alt.
beschreibe_person("Alice", 25)  # Ausgabe: Alice ist 25 Jahre alt.
