cars = ["Ford", "Volvo", "BMW"]
print(cars)

# Element hinzufügen
cars.append("Mercedes")
print(cars)
# Element entfernen
cars.remove("Volvo")
print(cars)
# Element an einer bestimmten Position einfügen
cars.insert(1, "Audi")
print(cars)
# Element an einer bestimmten Position entfernen
cars.pop(2)
print(cars)
# Element an einer bestimmten Position ändern
cars[2] = "Opel"
print(cars)
# Anzahl der Elemente in der Liste
print(len(cars))
# Sortieren der Liste
cars.sort()
print(cars)
# Umkehren der Liste
cars.reverse()
print(cars)
# Liste löschen
cars.clear()
print(cars)
