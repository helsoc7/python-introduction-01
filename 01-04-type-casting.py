# String zu Ganzzahl
str_num = "42"
int_num = int(str_num)
print(int_num)  # Ausgabe: 42

# Ganzzahl zu String
age = 26
age_str = str(age)
print(age_str)  # Ausgabe: "26"

# Ganzzahl zu Gleitkommazahl
num = 7
float_num = float(num)
print(float_num)  # Ausgabe: 7.0

# Liste zu Tupel
lst = [1, 2, 3]
tpl = tuple(lst)
print(tpl)  # Ausgabe: (1, 2, 3)

# Gleitkommazahl zu Bool
flt = 0.0
bool_value = bool(flt)
print(bool_value)  # Ausgabe: False
